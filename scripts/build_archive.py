DATE_URL_RE = re.compile(r"/\d{4}/\d{2}/\d{2}/([^/?#]+)/?$")
NUMBER_PREFIX_RE = re.compile(r"^\s*\d+[\.\)]\s*")
LI_LINK_RE = re.compile(r"<li>\s*<a[^>]+href=\"([^\"]*)\"[^>]*>(.*?)</a>", re.IGNORECASE)


def clean_text(value: str | None) -> str:
    if not value:
        return ""
    text = html.unescape(value)
    text = text.replace("\xa0", " ")
    text = re.sub(r"\s+", " ", text)
    return text.strip()


def canonical_url(url: str) -> str:
    parsed = urlparse(url)
    path = parsed.path.rstrip("/") or "/"
    return f"https://byteseismic.com{path}"


def slug_from_url(url: str) -> str | None:
    match = DATE_URL_RE.search(url)
    if match:
        return match.group(1)
    parsed = urlparse(url)
    bits = [bit for bit in parsed.path.split("/") if bit]
    if bits:
        return bits[-1]
    return None


def slugify(text: str) -> str:
    lowered = clean_text(text).lower()
    lowered = lowered.replace("&", " and ")
    lowered = re.sub(r"[“”\"'`]+", "", lowered)
    lowered = re.sub(r"[^a-z0-9]+", "-", lowered)
    lowered = lowered.strip("-")
    return lowered or "untitled"


def strip_html(raw: str) -> str:
    return clean_text(BeautifulSoup(raw or "", "html.parser").get_text(" ", strip=True))


def strip_number_prefix(text: str) -> str:
    return NUMBER_PREFIX_RE.sub("", clean_text(text))


def looks_like_prompt(text: str) -> bool:
    cleaned = strip_number_prefix(text)
    lowered = cleaned.lower()
    return "?" in cleaned or lowered.startswith(PROMPT_STARTERS)


