def write_if_allowed(target: Path, contents: str) -> bool:
    if target.exists():
        existing = target.read_text()
        if AUTO_MARKER not in existing:
            return False
    target.parent.mkdir(parents=True, exist_ok=True)
    target.write_text(contents)
    return True


def cleanup_auto_generated(valid_targets: set[Path]) -> None:
    for target in ROOT.rglob("index.html"):
        if target in valid_targets:
            continue
        if AUTO_MARKER not in target.read_text():
            continue
        target.unlink()
        parent = target.parent
        while parent != ROOT and not any(parent.iterdir()):
            parent.rmdir()
            parent = parent.parent


def node_to_topic_tree(nodes: list[dict]) -> list[dict]:
    tree = []
    for node in nodes:
        entry = {"title": node["title"]}
        if node.get("built_path"):
            entry["path"] = node["built_path"]
        if node.get("children"):
            entry["children"] = node_to_topic_tree(node["children"])
        tree.append(entry)
    return tree


def flatten_visible_nodes(nodes: list[dict]) -> list[dict]:
    flat = []
    for node in nodes:
        flat.append(node)
        flat.extend(flatten_visible_nodes(node.get("children", [])))
    return flat


def main() -> None:
    home_html = fetch_homepage_html()
    section_nodes = section_nodes_from_home(home_html)
    all_posts = fetch_all_posts()

    visible_lookup: dict[str, dict] = {}
    for section_id, nodes in section_nodes.items():
        visible_lookup.update(hierarchy_lookup(nodes, section_id))

    posts_by_url = {canonical_url(post["URL"]): post for post in all_posts if post.get("URL")}
    generated_pages: list[dict] = []
    page_index: dict[tuple[str, str], dict] = {}

    def register_page(
        title: str,
        section_id: str,
        built_path: str,
        source_url: str = "",
        post: dict | None = None,
        children: list[dict] | None = None,
        sibling_titles: list[str] | None = None,
    ) -> dict:
        key = (section_id, built_path)
        if key in page_index:
            page = page_index[key]
            if children is not None:
                page["children"] = children
            if sibling_titles is not None:
                page["sibling_titles"] = sibling_titles
            return page

        source_prompts, thread_like, _paragraphs = ([], [], [])
        source_prompt_details: list[dict] = []
        if post:
            source_prompts, thread_like, _paragraphs = normalize_heading_list(
                post.get("content", ""),
                clean_text(title),
            )
            source_prompt_details = extract_source_prompt_details(
                post.get("content", ""),
                clean_text(title),
            )
            if source_prompt_details:
                source_prompts = [detail["prompt"] for detail in source_prompt_details]
        page = {
            "title": clean_text(title),
            "section_id": section_id,
            "built_path": built_path,
            "source_url": source_url,
            "kind": infer_kind(title, bool(children)),
            "source_prompts": source_prompts,
            "source_prompt_details": source_prompt_details,
            "thread_like": thread_like,
            "excerpt": strip_html(post.get("excerpt", "") if post else ""),
            "date": post.get("date", "") if post else "",
            "children": children or [],
            "sibling_titles": sibling_titles or [],
        }
        page_index[key] = page
        generated_pages.append(page)
        return page

    def assign_visible_paths(section_id: str, nodes: list[dict], siblings: list[str] | None = None) -> None:
        sibling_titles = siblings or [node["title"] for node in nodes]
        for node in nodes:
            href = node.get("href", "")
            canonical = canonical_url(href) if href and "byteseismic.com" in href else ""
            post = posts_by_url.get(canonical)
            synthetic = not post and (not href or "wordpress.com" in href or "#" in href)
            built_path = build_path_for_node(section_id, node["title"], source_url=href, synthetic=synthetic)
            node["built_path"] = built_path
            if post:
                register_page(
                    title=node["title"],
                    section_id=section_id,
                    built_path=built_path,
                    source_url=canonical,
                    post=post,
                    children=node.get("children", []),
                    sibling_titles=sibling_titles,
                )
            else:
                register_page(
                    title=node["title"],
                    section_id=section_id,
                    built_path=built_path,
                    source_url=canonical or href,
                    children=node.get("children", []),
                    sibling_titles=sibling_titles,
                )
            if node.get("children"):
                assign_visible_paths(section_id, node["children"], [child["title"] for child in node["children"]])

    for section_id, nodes in section_nodes.items():
        assign_visible_paths(section_id, nodes)

    visible_urls = {canonical_url(post["URL"]) for post in all_posts if canonical_url(post["URL"]) in visible_lookup}

    for post in all_posts:
        canonical = canonical_url(post["URL"])
        if canonical in visible_urls:
            continue
        override = FEATURED_PATHS_BY_TITLE.get(clean_text(post["title"]).lower())
        if override:
            section_id, built_path = override
        else:
            section_id = classify_section(post)
            built_path = f"/{section_id}/{slugify(post['slug'])}/"
        register_page(
            title=clean_text(post["title"]),
            section_id=section_id,
            built_path=built_path,
            source_url=canonical,
            post=post,
        )

    generated_pages.sort(key=lambda item: (item["section_id"], item["title"].lower()))

    valid_targets: set[Path] = set()
    for page in generated_pages:
        if page["built_path"] in GROUP_PAGE_TITLES.values():
            continue
        target = ROOT / page["built_path"].strip("/") / "index.html"
        valid_targets.add(target)
        write_if_allowed(target, render_article_page(page))

    menu_target = ROOT / "menu-structure" / "index.html"
    podcast_target = ROOT / "byteseismic-podcasts" / "index.html"
    archive_target = ROOT / "recent-posts-expanded-version" / "index.html"
    valid_targets.update({menu_target, podcast_target, archive_target})

    write_if_allowed(menu_target, render_menu_structure_page())
    write_if_allowed(podcast_target, render_podcast_page())

    posts_by_year: dict[int, list[dict]] = defaultdict(list)
    section_counts: dict[str, int] = defaultdict(int)
    for page in generated_pages:
        if not page["date"]:
            continue
        year = int(page["date"][:4])
        posts_by_year[year].append(page)
        section_counts[page["section_id"]] += 1

    write_if_allowed(archive_target, render_expanded_archive_page(posts_by_year, section_counts))

    def choose_featured_pages() -> list[dict]:
        chosen = []
        seen_sections = set()
        path_lookup = {page["built_path"]: page for page in generated_pages}
        for spec in FEATURED_PAGE_SPECS:
            page = path_lookup.get(spec["path"])
            section_meta = SECTION_META[spec["section_id"]]
            if page is None:
                pseudo_page = {
                    "title": spec["title"],
                    "section_id": spec["section_id"],
                    "kind": infer_kind(spec["title"], False),
                }
                chosen.append(
                    {
                        "title": spec["title"],
                        "section": section_meta["name"],
                        "path": spec["path"],
                        "summary": feature_summary(pseudo_page, section_meta),
                        "tags": tag_candidates(pseudo_page, section_meta),
                    }
                )
                seen_sections.add(spec["section_id"])
                continue

            chosen.append(
                {
                    "title": page["title"],
                    "section": section_meta["name"],
                    "path": spec["path"],
                    "summary": feature_summary(page, section_meta),
                    "tags": tag_candidates(page, section_meta),
                }
            )
            seen_sections.add(spec["section_id"])
        for section_id in SECTION_IDS:
            if section_id in seen_sections:
                continue
            section_pages = [page for page in generated_pages if page["section_id"] == section_id]
            if not section_pages:
                continue
            page = section_pages[0]
            section_meta = SECTION_META[section_id]
            chosen.append(
                {
                    "title": page["title"],
                    "section": section_meta["name"],
                    "path": page["built_path"],
                    "summary": feature_summary(page, section_meta),
                    "tags": tag_candidates(page, section_meta),
                }
            )
        return chosen

    featured_pages = choose_featured_pages()

    topic_paths = {
        "Site Map": "/menu-structure/",
        "Recent Posts — Expanded Version": "/recent-posts-expanded-version/",
    }
    for spec in FEATURED_PAGE_SPECS:
        topic_paths[spec["title"]] = spec["path"]
    sections_data = []
    for section_id in SECTION_IDS:
        meta = SECTION_META[section_id]
        nodes = section_nodes.get(section_id, [])
        topic_tree = node_to_topic_tree(nodes)
        flat_nodes = flatten_visible_nodes(nodes)
        for node in flat_nodes:
            if node.get("built_path"):
                topic_paths[node["title"]] = node["built_path"]
        visible_titles = [node["title"] for node in flat_nodes]
        sample_path = meta.get("preferredPath")
        if not sample_path and flat_nodes:
            sample_path = flat_nodes[0].get("built_path")
        sections_data.append(
            {
                "id": section_id,
                "name": meta["name"],
                "summary": meta["summary"],
                "seedTopics": visible_titles[:8],
                "topicCount": len(visible_titles),
                "futureTags": meta["futureTags"],
                "samplePath": sample_path,
                "topicTree": topic_tree,
            }
        )

    data = {
        "siteTitle": "Byteseismic Philosophy",
        "siteTagline": "Final compilations of philosophical ideas distilled from the curator's prompts and multiple AI interlocutors.",
        "featuredPages": featured_pages,
        "topicPaths": topic_paths,
        "sections": sections_data,
    }
    site_data_js = "window.BYTESEISMIC_DATA = " + json.dumps(data, indent=2, ensure_ascii=False) + ";\n"
    (ROOT / "assets" / "js" / "site-data.js").write_text(site_data_js)
    cleanup_auto_generated(valid_targets)

    print(f"Generated or updated {len(generated_pages)} content pages.")
    print(f"Visible hierarchy sections: {len(section_nodes)}")
    print(f"Expanded archive years: {len(posts_by_year)}")


if __name__ == "__main__":
    main()
