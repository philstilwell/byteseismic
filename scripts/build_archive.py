def fetch_posts(cache_path: Path, fields: str) -> list[dict]:
    if cache_path.exists():
        return json.loads(cache_path.read_text())

    session = requests.Session()
    offset = 0
    posts: list[dict] = []
    total_found = None

    while True:
        url = f"{POSTS_API}?number=100&offset={offset}&fields={fields}"
        response = session.get(url, timeout=60)
        response.raise_for_status()
        data = response.json()
        batch = data.get("posts", [])
        posts.extend(batch)
        total_found = total_found or data.get("found")
        if not batch or len(posts) >= (total_found or 0):
            break
        offset += len(batch)

    cache_path.write_text(json.dumps(posts, indent=2, ensure_ascii=False))
    return posts


def fetch_all_posts() -> list[dict]:
    return fetch_posts(POSTS_CACHE, POST_FIELDS)


def parse_topic_list(ul: Tag) -> list[dict]:
    nodes = []
    for li in ul.find_all("li", recursive=False):
        anchor = li.find("a", recursive=False) or li.find("a")
        title = clean_text(anchor.get_text(" ", strip=True) if anchor else li.get_text(" ", strip=True))
        href = clean_text(anchor.get("href")) if anchor and anchor.get("href") else ""
        child_ul = li.find("ul", recursive=False)
        children = parse_topic_list(child_ul) if child_ul else []
        nodes.append({"title": title, "href": href, "children": children})
    return nodes


def parse_malformed_topic_html(raw_html: str) -> list[dict]:
    prepared = (
        raw_html.replace("<ul", "\n<ul")
        .replace("</ul>", "\n</ul>\n")
        .replace("<li>", "\n<li>")
    )
    root: list[dict] = []
    stack: list[list[dict]] = [root]

    for line in prepared.splitlines():
        stripped = line.strip()
        if not stripped:
            continue
        if stripped.startswith("<ul"):
            current = stack[-1]
            if current:
                current[-1].setdefault("children", [])
                stack.append(current[-1]["children"])
            else:
                stack.append(current)
            continue
        if stripped.startswith("</ul"):
            if len(stack) > 1:
                stack.pop()
            continue
        match = LI_LINK_RE.search(stripped)
        if match:
            href = clean_text(match.group(1))
            title = clean_text(BeautifulSoup(match.group(2), "html.parser").get_text(" ", strip=True))
            stack[-1].append({"title": title, "href": href, "children": []})

    return root


def is_anchor_only(href: str, heading_id: str) -> bool:
    return bool(href and f"#{heading_id}" in href)


def parse_detail_nodes(detail: Tag, section_id: str, heading_id: str) -> list[dict]:
    summary_tag = detail.find("summary")
    summary = clean_text(summary_tag.get_text(" ", strip=True) if summary_tag else "")
    nodes = parse_malformed_topic_html(str(detail))
    if not nodes:
        return []

    if summary.lower() == "toggle section on/off":
        if (
            nodes
            and nodes[0]["title"].upper() == SECTION_META[section_id]["name"].upper()
            and nodes[0].get("children")
        ):
            nodes = nodes[0]["children"]

        filtered = []
        for node in nodes:
            same_section = node["title"].upper() == SECTION_META[section_id]["name"].upper()
            anchor_only = is_anchor_only(node.get("href", ""), heading_id)
            if same_section and anchor_only:
                continue
            filtered.append(node)
        return filtered

    root = nodes[0]
    if root.get("children"):
        href = root.get("href", "")
        title = summary or root["title"]
        return [
            {
                "title": title,
                "href": "" if is_anchor_only(href, heading_id) else href,
                "children": root["children"],
            }
        ]

    return [{"title": summary or root["title"], "href": "", "children": nodes}]


def section_nodes_from_home(html_text: str) -> dict[str, list[dict]]:
    soup = BeautifulSoup(html_text, "html.parser")
    sections: dict[str, list[dict]] = {}
    for heading in soup.select("h4[id]"):
        section_id = heading["id"].lower()
        if section_id not in SECTION_META:
            continue
        details = []
        for element in heading.next_elements:
            if isinstance(element, Tag) and element.name == "h4" and element is not heading and element.get("id"):
                break
            if isinstance(element, Tag) and element.name == "details":
                details.append(element)

        if not details:
            continue

        nodes: list[dict] = []
        for detail in details:
            nodes.extend(parse_detail_nodes(detail, section_id, heading["id"]))
        sections[section_id] = nodes
    return sections


def flatten_titles(nodes: Iterable[dict]) -> list[str]:
    titles: list[str] = []
    for node in nodes:
        titles.append(node["title"])
        titles.extend(flatten_titles(node.get("children", [])))
    return titles


def hierarchy_lookup(nodes: list[dict], section_id: str, parents: list[str] | None = None) -> dict[str, dict]:
    parents = parents or []
    lookup: dict[str, dict] = {}
    for node in nodes:
        href = node.get("href", "")
        title = node["title"]
        if href and "byteseismic.com" in href and not href.endswith("/feed/"):
            lookup[canonical_url(href)] = {
                "section_id": section_id,
                "parents": parents[:],
                "title": title,
            }
        lookup.update(hierarchy_lookup(node.get("children", []), section_id, parents + [title]))
    return lookup


