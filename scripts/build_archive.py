def append_unique_paragraph(paragraphs: list[str], candidate: str) -> None:
    normalized_candidate = clean_text(candidate).lower()
    if not normalized_candidate:
        return
    normalized_existing = {clean_text(paragraph).lower() for paragraph in paragraphs}
    if normalized_candidate not in normalized_existing:
        paragraphs.append(candidate)


def editorial_polish_content(
    page: dict,
    prompt: str,
    prompts: list[str],
    index: int,
    detail: dict | None,
    paragraphs: list[str],
    list_items: list[str],
) -> tuple[list[str], list[str]]:
    focus = prompt_focus(prompt)

    polished_items = [
        enriched
        for item in list_items
        if (enriched := enrich_support_item(item, page, prompt, focus))
    ]
    polished_items = dedupe(polished_items)
    for item in supplemental_support_items(page, prompt, detail, focus):
        if len(polished_items) >= 5 and average_item_words(polished_items) >= 13:
            break
        polished_items.append(enrich_support_item(item, page, prompt, focus) or item)
        polished_items = dedupe(polished_items)

    polished_paragraphs = list(paragraphs)
    append_unique_paragraph(polished_paragraphs, sequence_context_paragraph(page, prompt, prompts, index))
    append_unique_paragraph(polished_paragraphs, intermediate_reader_paragraph(page, prompt, focus))
    if len(polished_paragraphs) < 5 or content_word_count(polished_paragraphs, polished_items) < 320:
        append_unique_paragraph(polished_paragraphs, editorial_insight_paragraph(page, prompt, focus))
    if content_word_count(polished_paragraphs, polished_items) < 320:
        append_unique_paragraph(polished_paragraphs, reader_test_paragraph(page, prompt, focus))
    if page["section_id"] == "philosophers" or content_word_count(polished_paragraphs, polished_items) < 360:
        append_unique_paragraph(polished_paragraphs, exceptional_editorial_paragraph(page, prompt, focus))

    return polished_paragraphs[:7], polished_items[:6]


def content_word_count(paragraphs: list[str], list_items: list[str]) -> int:
    return sum(len(clean_text(part).split()) for part in paragraphs + list_items)


def average_item_words(list_items: list[str]) -> float:
    return sum(len(clean_text(item).split()) for item in list_items) / max(len(list_items), 1)


def polish_review_content(
    page: dict,
    prompt: str,
    detail: dict | None,
    paragraphs: list[str],
    list_items: list[str],
) -> tuple[list[str], list[str]]:
    focus = prompt_focus(prompt)
    topic = topic_label(page["title"])
    profile = branch_profile(page["section_id"])
    key = short_prompt_key(prompt, topic)

    polished_items = [
        enriched
        for item in list_items
        if (enriched := enrich_support_item(item, page, prompt, focus))
    ]
    polished_items = dedupe(polished_items)

    for item in supplemental_support_items(page, prompt, detail, focus):
        if len(polished_items) >= 4 and average_item_words(polished_items) >= 12:
            break
        polished_items.append(enrich_support_item(item, page, prompt, focus) or item)
        polished_items = dedupe(polished_items)

    polished_paragraphs = list(paragraphs)
    if len(polished_paragraphs) < 3 or content_word_count(polished_paragraphs, polished_items) < 170:
        polished_paragraphs.append(
            f"The answer therefore has to do three jobs at once: identify the claim about {key}, "
            f"show why that claim matters inside {SECTION_META[page['section_id']]['name']}, and mark the point at which a reasonable reader could still resist it."
        )
    if len(polished_paragraphs) < 4 and content_word_count(polished_paragraphs, polished_items) < 210:
        polished_paragraphs.append(
            f"That extra pressure keeps the section from becoming a label or a slogan. It asks whether {topic} clarifies {profile['lens']}, "
            f"and it gives the reader handles for further revision instead of a closed verdict."
        )

    return polished_paragraphs[:4], polished_items[:6]


def section_list_items(page: dict, index: int, prompt: str, detail: dict | None = None) -> list[str]:
    sourced_items = source_detail_list_items(detail, page)
    if sourced_items:
        return sourced_items

    threads = usable_thread_items(page.get("thread_like", []))
    focus = prompt_focus(prompt)
    child_titles = [child["title"] for child in page.get("children", [])]
    topic_items = TOPIC_ITEMS.get(page["title"], [])

    if focus in {"description", "dialogue"} and topic_items:
        return topic_items[:4]

    if focus == "examples":
        examples = [
            item
            for item in threads
            if "–" in item or " - " in item or re.search(r"\b(Case|Scenario|Einstein|Popper|Curie|Darwin|Hume|Kant|Aquinas)\b", item)
        ]
        if examples:
            return examples[:4]

    if focus == "mapping":
        if "contribution" in prompt.lower() and topic_items:
            return topic_items[:4]
        if child_titles and "percentage" not in prompt.lower() and "rough estimates" not in prompt.lower():
            return child_titles[:4]
        mapped = [
            item
            for item in threads
            if not item.lower().startswith(("conclusion", "introduction"))
        ]
        if mapped:
            return mapped[:4]

    start = max(0, (index - 1) * 3)
    items = threads[start : start + 4]
    if items:
        return items
    return branch_profile(page["section_id"])["items"]


def detail_metric_counts(detail: dict | None) -> dict[str, int]:
    if not detail:
        return {"children": 0, "sourceParagraphs": 0, "sourceItems": 0, "labels": 0}
    source_paragraphs = len(detail.get("paragraphs", []))
    source_items = len(detail.get("items", []))
    for child in detail.get("children", []):
        source_paragraphs += len(child.get("paragraphs", []))
        source_items += len(child.get("items", []))
    return {
        "children": len(detail.get("children", [])),
        "sourceParagraphs": source_paragraphs,
        "sourceItems": source_items,
        "labels": len(source_detail_labels(detail)),
    }


def quality_level(score: int) -> str:
    if score >= 96:
        return "exceptional"
    if score >= 85:
        return "strong"
    if score >= 72:
        return "good"
    if score >= 60:
        return "developing"
    return "thin"


def quality_assessment(
    page: dict,
    prompt: str,
    detail: dict | None,
    paragraphs: list[str],
    list_items: list[str],
) -> dict:
    metrics = detail_metric_counts(detail)
    word_count = sum(len(clean_text(part).split()) for part in paragraphs + list_items)
    avg_item_words = round(
        sum(len(clean_text(item).split()) for item in list_items) / max(len(list_items), 1),
        1,
    )
    claim = first_source_claim(detail)
    robust_reconstruction = (
        word_count >= 170
        and len(paragraphs) >= 3
        and len(list_items) >= 4
        and avg_item_words >= 10
    )
    editorial_depth = (
        word_count >= 300
        and len(paragraphs) >= 5
        and len(list_items) >= 5
        and avg_item_words >= 12
    )
    exceptional_ready = bool(
        word_count >= 360
        and len(paragraphs) >= 5
        and len(list_items) >= 5
        and avg_item_words >= 16
        and any("For an intermediate reader" in paragraph for paragraph in paragraphs)
        and (claim or metrics["labels"] >= 2 or metrics["sourceParagraphs"] >= 2 or metrics["sourceItems"] >= 4)
    )
    philosopher_gap = page["section_id"] == "philosophers"
    continuity_scaffold = any("For an intermediate reader" in paragraph for paragraph in paragraphs)
    reasons: list[str] = []
    gap_reasons: list[str] = []

    score = 18
    if detail:
        score += 12
    else:
        reasons.append("No source prompt structure was recovered; this section relies on branch-level reconstruction.")

    score += min(20, metrics["labels"] * 5)
    score += min(14, metrics["sourceParagraphs"] * 2)
    score += min(10, metrics["sourceItems"])
    score += min(12, len(list_items) * 3)
    score += min(10, len(paragraphs) * 3)

    if claim:
        score += 8
    elif robust_reconstruction:
        score += 8
        reasons.append("The source claim was sparse, so this section was expanded with explicit reconstruction supports.")
    else:
        reasons.append("No clear source claim was extracted.")

    if word_count >= 170:
        score += 8
    elif word_count < 95:
        score -= 10
        reasons.append("The reconstructed response is short enough to need hand expansion.")

    if editorial_depth:
        score += 14
    else:
        score -= 4
        reasons.append("The section is below the current editorial depth target.")

    if continuity_scaffold:
        score += 4

    if len(list_items) <= 1:
        score -= 8
        reasons.append("The section has one or fewer concrete support items.")
    if metrics["labels"] <= 1 and metrics["sourceParagraphs"] <= 1 and not robust_reconstruction:
        score -= 6
        reasons.append("The source material provides little internal hierarchy.")
    elif metrics["labels"] <= 1 and metrics["sourceParagraphs"] <= 1 and robust_reconstruction:
        reasons.append("Source hierarchy was sparse, so the reconstruction supplies explicit support structure.")
    if avg_item_words < 8 and list_items:
        score -= 5
        reasons.append("Support items are mostly labels rather than explanatory claims.")

    score = max(0, min(100, score))
    level = quality_level(score)
    if level in {"thin", "developing"} and not reasons:
        reasons.append("This section is adequate scaffolding but should be considered for a hand-polish pass.")
    if level in {"good", "strong", "exceptional"} and not reasons:
        reasons.append("The section has enough source structure and explanatory density for now.")

    if not exceptional_ready:
        if word_count < 360:
            gap_reasons.append("Needs a little more argumentative texture before it reaches the exceptional bar.")
        if avg_item_words < 16:
            gap_reasons.append("Support items should become more explanatory and less list-like.")
        if metrics["labels"] <= 1 and metrics["sourceParagraphs"] <= 1:
            gap_reasons.append("Source hierarchy is sparse; later hand-curation should add examples, objections, or context.")
    if philosopher_gap:
        gap_reasons.append("Philosopher page needs eventual primary-source texture, historical setting, and influence trail.")
    needs_gap_fill = (not exceptional_ready) or philosopher_gap

    return {
        "score": score,
        "level": level,
        "needsReview": level in {"thin", "developing"} or not editorial_depth,
        "exceptionalReady": exceptional_ready,
        "needsGapFill": needs_gap_fill,
        "gapReasons": gap_reasons[:3],
        "wordCount": word_count,
        "avgItemWords": avg_item_words,
        "metrics": metrics,
        "reasons": reasons[:3],
    }


def source_prompt_sections(page: dict, prompts: list[str]) -> list[dict]:
    sections = []
    source_details = page.get("source_prompt_details", [])
    for index, prompt in enumerate(prompts, start=1):
        detail = source_details[index - 1] if index - 1 < len(source_details) else None
        paragraphs = prompt_response_paragraphs(page, prompt, index, detail)
        list_items = section_list_items(page, index, prompt, detail)
        paragraphs, list_items = editorial_polish_content(
            page,
            prompt,
            prompts,
            index,
            detail,
            paragraphs,
            list_items,
        )
        quality = quality_assessment(page, prompt, detail, paragraphs, list_items)
        if quality["needsReview"] or quality["score"] < 85:
            paragraphs, list_items = polish_review_content(page, prompt, detail, paragraphs, list_items)
            paragraphs, list_items = editorial_polish_content(
                page,
                prompt,
                prompts,
                index,
                detail,
                paragraphs,
                list_items,
            )
            quality = quality_assessment(page, prompt, detail, paragraphs, list_items)
        sections.append(
            {
                "id": f"prompt-{index}",
                "eyebrow": "Composite Response",
                "heading": source_prompt_heading(prompt, topic_label(page["title"]), detail),
                "paragraphs": paragraphs,
                "list_items": list_items,
                "prompt": prompt,
                "quality": quality,
            }
        )

    profile = branch_profile(page["section_id"])
    topic_items = TOPIC_ITEMS.get(page["title"], [])
    synthesis_paragraphs = [
        profile["route"],
        profile["pressure"],
    ]
    if topic_items:
        synthesis_paragraphs.append(
            f"For this topic, the durable pressure points include {', '.join(topic_items[:4])}."
        )
    synthesis_paragraphs.append(
        f"Read this page as part of the wider {SECTION_META[page['section_id']]['name']} branch: the prompts point inward to the topic, but they also point outward to neighboring questions that keep the topic honest."
    )

    sections.append(
        {
            "id": "synthesis",
            "eyebrow": "Synthesis",
            "heading": f"The through-line is what this topic asks the reader to notice.",
            "paragraphs": synthesis_paragraphs,
            "question_items": discussion_questions(page, page.get("thread_like", [])),
        }
    )
    return sections


