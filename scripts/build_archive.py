    excerpt = strip_html(post.get("excerpt", ""))
    tag_blob = " ".join(clean_text(tag_name) for tag_name in (post.get("tags") or {}).keys())
    corpus = f"{title} {excerpt} {tag_blob}".lower()

    best_section = "miscellany"
    best_score = 0
    for section_id, keywords in CLASSIFIER_KEYWORDS.items():
        score = sum(1 for keyword in keywords if keyword in corpus)
        if score > best_score:
            best_score = score
            best_section = section_id
    return best_section


