def source_prompt_heading(prompt: str, topic: str, detail: dict | None) -> str:
    labels = source_detail_labels(detail)
    if labels:
        center = labels[0].strip(" .:")
        return article_native_heading(center, prompt, topic)

    key = short_prompt_key(prompt, topic)
    if key and key.lower() != topic.lower():
        return article_native_heading(key, prompt, topic)
    return prompt_heading(prompt, topic)


def first_source_claim(detail: dict | None) -> str:
    if not detail:
        return ""
    candidates: list[str] = []
    candidates.extend(detail.get("paragraphs", []))
    candidates.extend(detail.get("items", []))
    for child in detail.get("children", []):
        candidates.extend(child.get("paragraphs", []))
        candidates.extend(child.get("items", []))
    for candidate in candidates:
        if looks_like_prompt(candidate) or is_low_value_prompt(candidate):
            continue
        claim = rewrite_source_sentence(candidate)
        if claim:
            return claim
    return ""


def prompt_focus(prompt: str) -> str:
    lowered = prompt.lower()
    if "dialogue" in lowered:
        return "dialogue"
    if "general description" in lowered or "description of" in lowered:
        return "description"
    if (
        "example" in lowered
        or "anecdote" in lowered
        or "case study" in lowered
        or re.search(r"\bcase\s*#?\d+\b", lowered)
        or re.search(r"\bscenario\b", lowered)
    ):
        return "examples"
    if (
        "list" in lowered
        or "identify" in lowered
        or "primary areas" in lowered
        or "contributions" in lowered
        or "percentage" in lowered
        or "rough estimates" in lowered
        or "for each" in lowered
    ):
        return "mapping"
    if "essay" in lowered or "comment" in lowered or "weigh" in lowered:
        return "argument"
    if lowered.startswith(("what is", "what are")):
        return "definition"
    return "inquiry"


def prompt_heading(prompt: str, topic: str) -> str:
    focus = prompt_focus(prompt)
    if focus == "dialogue":
        return f"A dialogue makes {topic} answerable to another mind."
    if focus == "description":
        return f"A useful description should reveal the working center of {topic}."
    if focus == "examples":
        return f"Examples make {topic} visible in practice."
    if focus == "mapping":
        return f"The map of {topic} matters only if it reveals the structure."
    if focus == "argument":
        return f"The argument about {topic} has to expose its pressure points."
    if focus == "definition":
        return f"A definition of {topic} has to do real work."
    return f"The prompt turns {topic} into a question that can be tested."


