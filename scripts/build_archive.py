def usable_thread_items(items: Iterable[str]) -> list[str]:
    result = []
    for item in items:
        item = remove_source_artifact_phrases(rewrite_unavailable_asset_references(item))
        lowered = clean_text(item).lower()
        if not item or looks_like_prompt(item) or is_low_value_prompt(item):
            continue
        if is_source_artifact_label(item):
            continue
        if lowered in {"conclusion", "questions", "introduction"}:
            continue
        if lowered.startswith(("conclusion", "questions")):
            continue
        result.append(item)
    return result


def source_detail_list_items(detail: dict | None, page: dict) -> list[str]:
    if not detail:
        return []

    items: list[str] = []
    children = detail.get("children", [])
    for child in children:
        label = rewrite_unavailable_asset_references(strip_number_prefix(child.get("title", ""))).strip(" .:")
        label = display_label(label)
        if is_source_artifact_label(label):
            continue
        if not label:
            continue
        if len(children) == 1 and "example" in label.lower() and child.get("paragraphs"):
            for paragraph in child["paragraphs"][:6]:
                item_label, item_body = split_label(paragraph)
                if item_label and item_body:
                    items.append(f"{item_label}: {rewrite_source_sentence(item_body)}")
                else:
                    items.append(rewrite_source_sentence(paragraph))
            continue
        body_source = ""
        if child.get("paragraphs"):
            body_source = child["paragraphs"][0]
        elif child.get("items"):
            body_source = child["items"][0]
        if body_source:
            items.append(f"{label}: {rewrite_source_sentence(body_source)}")
        else:
            items.append(f"{label}: This is one of the structural moves the prompt asks the reader to keep distinct.")

    for raw_item in detail.get("items", []):
        if looks_like_prompt(raw_item) or is_low_value_prompt(raw_item):
            continue
        label, body = split_label(raw_item)
        label = rewrite_unavailable_asset_references(label)
        label = display_label(label)
        if label and is_source_artifact_label(label):
            continue
        if label.lower() in {"pros", "cons"} and len(items) >= 3:
            continue
        if label and body:
            items.append(f"{label}: {rewrite_source_sentence(body)}")
        else:
            items.append(rewrite_source_sentence(raw_item))

    return dedupe([item for item in items if item])[:6]


