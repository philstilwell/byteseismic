def build_quality_report(generated_pages: list[dict]) -> dict:
    records = []
    for page in generated_pages:
        if page["built_path"] in GROUP_PAGE_TITLES.values():
            continue
        if page.get("quality_tracked") is False:
            continue
        for section in compose_sections(page):
            quality = section.get("quality")
            if not quality:
                continue
            records.append(
                {
                    "sectionId": page["section_id"],
                    "sectionName": SECTION_META[page["section_id"]]["name"],
                    "pageTitle": page["title"],
                    "pagePath": page["built_path"],
                    "anchor": section["id"],
                    "url": f'{page["built_path"]}#{section["id"]}',
                    "prompt": section.get("prompt", ""),
                    "heading": section["heading"],
                    "score": quality["score"],
                    "level": quality["level"],
                    "needsReview": quality["needsReview"],
                    "exceptionalReady": quality["exceptionalReady"],
                    "needsGapFill": quality["needsGapFill"],
                    "gapReasons": quality["gapReasons"],
                    "wordCount": quality["wordCount"],
                    "avgItemWords": quality["avgItemWords"],
                    "metrics": quality["metrics"],
                    "reasons": quality["reasons"],
                }
            )

    by_section: dict[str, list[dict]] = defaultdict(list)
    for record in records:
        by_section[record["sectionId"]].append(record)

    summaries = []
    for section_id in SECTION_IDS:
        section_records = by_section.get(section_id, [])
        if not section_records:
            continue
        level_counts = {level: 0 for level in ("exceptional", "strong", "good", "developing", "thin")}
        for record in section_records:
            level_counts[record["level"]] += 1
        total = len(section_records)
        needs_review = sum(1 for record in section_records if record["needsReview"])
        needs_gap_fill = sum(1 for record in section_records if record["needsGapFill"])
        average_score = round(sum(record["score"] for record in section_records) / total, 1)
        summaries.append(
            {
                "sectionId": section_id,
                "sectionName": SECTION_META[section_id]["name"],
                "total": total,
                "averageScore": average_score,
                "needsReview": needs_review,
                "needsReviewRate": round(needs_review / total, 3),
                "needsGapFill": needs_gap_fill,
                "needsGapFillRate": round(needs_gap_fill / total, 3),
                "levels": level_counts,
                "weakest": sorted(section_records, key=lambda item: (item["score"], item["pageTitle"]))[:12],
            }
        )

    summaries.sort(key=lambda item: (-item["needsReviewRate"], item["averageScore"], item["sectionName"]))
    weakest = sorted(records, key=lambda item: (item["score"], item["sectionName"], item["pageTitle"]))[:150]
    gap_backlog = sorted(
        [record for record in records if record["needsGapFill"]],
        key=lambda item: (item["score"], item["sectionName"], item["pageTitle"]),
    )[:200]
    total_records = len(records)
    total_needs_review = sum(1 for record in records if record["needsReview"])
    total_gap_fill = sum(1 for record in records if record["needsGapFill"])

    return {
        "generatedAt": "2026-05-20",
        "overall": {
            "totalPromptSections": total_records,
            "averageScore": round(sum(record["score"] for record in records) / max(total_records, 1), 1),
            "needsReview": total_needs_review,
            "needsReviewRate": round(total_needs_review / max(total_records, 1), 3),
            "needsGapFill": total_gap_fill,
            "needsGapFillRate": round(total_gap_fill / max(total_records, 1), 3),
        },
        "editorialAudit": build_editorial_audit(records),
        "sectionSummaries": summaries,
        "weakest": weakest,
        "gapBacklog": gap_backlog,
        "records": records,
    }


def editorial_audit_issues(record: dict) -> list[str]:
    heading = clean_text(record.get("heading", ""))
    prompt = clean_text(record.get("prompt", ""))
    combined = f"{heading}\n{prompt}"
    issues: list[str] = []

    if re.search(r"\b(is|are) the pressure point\.$", heading, flags=re.IGNORECASE):
        issues.append("legacy pressure-point heading")
    if re.match(r"^(provide|create|list|produce|line hypothetical)\b", heading, flags=re.IGNORECASE):
        issues.append("work-order heading")
    if re.search(r"\b(the this|the the|an a|is it likely that is it|you response|to pushback|discussion question based on)\b", combined, flags=re.IGNORECASE):
        issues.append("grammar scar")
    if re.search(r"\b(attached|uploaded|provided screenshot|following images?)\b", combined, flags=re.IGNORECASE):
        issues.append("unsupported asset reference")
    if len(heading) > 128:
        issues.append("overlong heading")
    if "gives the response its center of gravity" in heading:
        issues.append("stale generated heading")
    return dedupe(issues)


def build_editorial_audit(records: list[dict]) -> dict:
    issue_counts: dict[str, int] = defaultdict(int)
    samples: list[dict] = []
    for record in records:
        issues = editorial_audit_issues(record)
        if not issues:
            continue
        for issue in issues:
            issue_counts[issue] += 1
        if len(samples) < 40:
            samples.append(
                {
                    "url": record["url"],
                    "sectionName": record["sectionName"],
                    "pageTitle": record["pageTitle"],
                    "anchor": record["anchor"],
                    "heading": record["heading"],
                    "issues": issues,
                }
            )

    return {
        "totalIssues": sum(issue_counts.values()),
        "affectedSections": len(samples),
        "issueCounts": dict(sorted(issue_counts.items())),
        "samples": samples,
    }


