def curated_source_prompts(page: dict) -> list[str]:
    prompts = []
    for prompt in page.get("source_prompts", []):
        clarified = clarify_prompt(prompt)
        if not clarified or is_low_value_prompt(clarified):
            continue
        normalized = re.sub(r"[^a-z0-9]+", " ", clarified.lower()).strip()
        if any(normalized and normalized in re.sub(r"[^a-z0-9]+", " ", existing.lower()) for existing in prompts):
            continue
        prompts.append(clarified)
    return dedupe(prompts)[:5]


def topic_label(title: str) -> str:
    cleaned = clean_text(title)
    if cleaned in TOPIC_ALIASES:
        return TOPIC_ALIASES[cleaned]
    patterns = [
        r"^What is\s+",
        r"^What are\s+",
        r"^Dialoguing with\s+",
        r"^Charting\s+",
        r"^Dangers:\s+",
        r"^Case #\d+\s*[-–]\s*",
    ]
    for pattern in patterns:
        cleaned = re.sub(pattern, "", cleaned, flags=re.IGNORECASE)
    return cleaned.strip(" ?")


def infer_kind(title: str, has_children: bool) -> str:
    lowered = clean_text(title).lower()
    if title in GROUP_PAGE_TITLES:
        return "special"
    if lowered.startswith("dialoguing with "):
        return "dialogue"
    if lowered.startswith("charting "):
        return "chart"
    if lowered.startswith("dangers:"):
        return "danger"
    if "core concepts" in lowered:
        return "core"
    if lowered.startswith("what is ") or lowered.startswith("what are "):
        return "concept"
    if has_children:
        return "cluster"
    if " vs " in lowered or " and " in lowered or "&" in title:
        return "comparison"
    return "essay"


def normalized_phrase(text: str) -> str:
    return re.sub(r"[^a-z0-9]+", " ", clean_text(text).lower()).strip()


def compound_tag_candidates(topic: str) -> tuple[list[str], set[str]]:
    normalized_topic = normalized_phrase(topic)
    phrase_tags: list[str] = []
    suppressed_components: set[str] = set()

    for phrase in sorted(COMPOUND_TAG_PHRASES, key=len, reverse=True):
        normalized = normalized_phrase(phrase)
        if not normalized:
            continue
        if not re.search(rf"\b{re.escape(normalized)}\b", normalized_topic):
            continue
        phrase_tags.append(slugify(normalized))
        suppressed_components.update(
            slugify(word)
            for word in normalized.split()
            if len(word) > 3
        )

    return dedupe(phrase_tags), suppressed_components


def tag_candidates(page: dict, section_meta: dict) -> list[str]:
    topic = topic_label(page["title"])
    phrase_tags, suppressed_components = compound_tag_candidates(topic)
    base = [
        slugify(word)
        for word in re.split(r"[\s/&:—–-]+", topic)
        if len(word) > 3 and slugify(word) not in suppressed_components
    ]
    tags = dedupe(phrase_tags + base + list(section_meta["futureTags"]))
    return tags[:5]


