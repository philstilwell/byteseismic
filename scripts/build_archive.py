def hierarchy_lookup(nodes: list[dict], section_id: str, parents: list[str] | None = None) -> dict[str, dict]:
    parents = parents or []
    lookup: dict[str, dict] = {}
    for node in nodes:
        href = node.get("href", "")
        title = node["title"]
        if href and "byteseismic.com" in href and not href.endswith("/feed/"):
            lookup[canonical_url(href)] = {
                "section_id": section_id,
                "parents": parents[:],
                "title": title,
            }
        lookup.update(hierarchy_lookup(node.get("children", []), section_id, parents + [title]))
    return lookup


def classify_section(post: dict) -> str:
    title = clean_text(post.get("title"))
    excerpt = strip_html(post.get("excerpt", ""))
    tag_blob = " ".join(clean_text(tag_name) for tag_name in (post.get("tags") or {}).keys())
    corpus = f"{title} {excerpt} {tag_blob}".lower()

    best_section = "miscellany"
    best_score = 0
    for section_id, keywords in CLASSIFIER_KEYWORDS.items():
        score = sum(1 for keyword in keywords if keyword in corpus)
        if score > best_score:
            best_score = score
            best_section = section_id
    return best_section


