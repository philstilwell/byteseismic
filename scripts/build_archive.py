def fetch_posts(cache_path: Path, fields: str) -> list[dict]:
    if cache_path.exists():
        return json.loads(cache_path.read_text())

    session = requests.Session()
    offset = 0
    posts: list[dict] = []
    total_found = None

    while True:
        url = f"{POSTS_API}?number=100&offset={offset}&fields={fields}"
        response = session.get(url, timeout=60)
        response.raise_for_status()
        data = response.json()
        batch = data.get("posts", [])
        posts.extend(batch)
        total_found = total_found or data.get("found")
        if not batch or len(posts) >= (total_found or 0):
            break
        offset += len(batch)

    cache_path.write_text(json.dumps(posts, indent=2, ensure_ascii=False))
    return posts


